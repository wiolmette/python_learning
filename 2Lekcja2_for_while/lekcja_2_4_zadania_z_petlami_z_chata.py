# 1. Napisz program, który wykorzystując pętlę for, obliczy sumę liczb od 1 do 100.
suma = 0
for i in range(1,101):
    suma += i  # to samo co suma = suma + i !!!
print(suma)

# Napisz program, który obliczy sumę kwadratów 
# liczb od 1 do 50. Wynik ma być wypisany na końcu.
suma2 = 0
for j in range (1, 51):
    suma2 = suma2 + j**2
print(suma2)

# Napisz program, który obliczy sumę sześcianów 
# liczb od 1 do 30. Wynik ma być wypisany na końcu.
suma3 = 0
for k in range (1, 31):
    suma3 = suma3 + k**3
print(suma3)
    


